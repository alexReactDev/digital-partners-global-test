export default function getMonth(month) {
	switch(month) {
		case 0: return "Янв.";
		case 1: return "Февр.";
		case 2: return "Март";
		case 3: return "Апр.";
		case 4: return "Май";
		case 5: return "Июнь";
		case 6: return "Июль";
		case 7: return "Авг.";
		case 8: return "Сент.";
		case 9: return "Окт.";
		case 10: return "Нояб.";
		case 11: return "Дек.";
	}
}